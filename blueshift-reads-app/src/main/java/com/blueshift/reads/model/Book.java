public class Book {
    private String sku;
    private String name;
    private String price;
    private String image_url;

    public String getSku() {
        return sku;
    }

    public String getName() {
        return name;
    }

    public String getPrice() {
        return price;
    }

    public String getImageUrl() {
        return image_url;
    }
}
